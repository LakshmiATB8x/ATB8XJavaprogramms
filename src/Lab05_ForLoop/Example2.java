package Lab05_ForLoop;

public class Example2 {

    public static void main(String[] args)
    {
        for(int i=1; i<=10;i++)
        {
            if(i==5)
            {
                System.out.println("This number can divide by 5");
            }
            else
            {
                System.out.println(i);
            }
        }
    }
}
