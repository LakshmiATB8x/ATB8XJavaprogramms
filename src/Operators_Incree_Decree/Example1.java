package Operators_Incree_Decree;

public class Example1 {
    public static void main(String[] args){
        int a=10;
        System.out.println(a);
        //doing invreement
        System.out.println(++a);//first increse value then it print

        //doing decreement
        int b= 6;
        System.out.println(b--);//first print value then decrese value
        System.out.println(b);
    }
}
