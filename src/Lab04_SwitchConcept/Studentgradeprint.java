package Lab04_SwitchConcept;

public class Studentgradeprint {
    public static void main(String[] args)
    {
        //Implement a switch statement that categorizes a student’s grade (A, B, C, D, F)
        // into “Excellent,” “Good,” “Average,” “Poor,” and “Fail.”
        char A='a';

        switch(A)
        {
            case 'a':
                System.out.println("Student Get A-Grade");
                break;

            case 'b':
                System.out.println("Student get B- Grade");
                break;

        }
    }

}
